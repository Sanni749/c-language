#include <stdio.h>
int main()
{
    int a = 5;
    printf("the value of left shife a<<1 is:%d", a << 1);
    printf("\nthe value of left shift a<<2 is:%d", a << 2);
    printf("\nthe value of right shift a>>1 is:%d", a >> 1);
    printf("\nthe value of right shift a>>2 is:%d", a >> 2);
    return 0;
}